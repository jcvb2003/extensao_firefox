// Observa mudanÃ§as no DOM para detectar PDFs
const observer = new MutationObserver(function(mutations) { 
    mutations.forEach(mutation => { 
        mutation.addedNodes.forEach(node => { 
            if (node.tagName === 'IFRAME') { 
                console.log('Novo iframe detectado:', node.src); 
                // Verifica se Ã© um PDF
                if (node.src && (node.src.includes('.pdf') || node.src.startsWith('blob:'))) {
                    setTimeout(() => checkForPDFs(), 500); // Pequeno delay para o PDF carregar
                }
            } 
            if (node.querySelectorAll) { 
                const iframes = node.querySelectorAll('iframe'); 
                iframes.forEach(iframe => { 
                    console.log('iframe encontrado em nÃ³ adicionado:', iframe.src); 
                    if (iframe.src && (iframe.src.includes('.pdf') || iframe.src.startsWith('blob:'))) {
                        setTimeout(() => checkForPDFs(), 500);
                    }
                }); 
            } 
        }); 
    }); 
}); 

// FunÃ§Ã£o para iniciar a detecÃ§Ã£o
function startPDFDetection() {
    console.log('Iniciando detecÃ§Ã£o de PDFs...');
    
    // Verifica imediatamente se jÃ¡ hÃ¡ PDFs
    if (!checkForPDFs()) {
        console.log('PDF nÃ£o encontrado imediatamente, iniciando observaÃ§Ã£o...');
        
        // Inicia a observaÃ§Ã£o do DOM
        observer.observe(document.body, { 
            childList: true, 
            subtree: true 
        });
        
        // Verifica periodicamente (fallback)
        const intervalCheck = setInterval(() => {
            if (pdfDetected) {
                clearInterval(intervalCheck);
                return;
            }
            checkForPDFs();
        }, 1000);
        
        // Para a verificaÃ§Ã£o apÃ³s 30 segundos
        setTimeout(() => {
            clearInterval(intervalCheck);
            if (!pdfDetected) {
                console.warn('PDF nÃ£o detectado apÃ³s 30 segundos');
            }
        }, 30000);
    }
}

